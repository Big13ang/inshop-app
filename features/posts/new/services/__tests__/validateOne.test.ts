import { validateOne } from '../validateOne';
import { parseImageHeader } from '../headerParser';

const PNG_1080_BUFFER = Uint8Array.from([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a,
  0x00, 0x00, 0x00, 0x0d,
  0x49, 0x48, 0x44, 0x52,
  0x00, 0x00, 0x04, 0x38, // width = 1080
  0x00, 0x00, 0x04, 0x38, // height = 1080
  0x08, 0x06, 0x00, 0x00, 0x00,
  0x0e, 0x80, 0xbd, 0x50,
  0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, 0x44,
  0xae, 0x42, 0x60, 0x82,
]);

describe('validateOne with 1080x1080 PNG', () => {
  it('validates 1080x1080 PNG file without rejection', async () => {
    const parse = parseImageHeader(PNG_1080_BUFFER.buffer);
    console.log('PARSE RESULT:', parse);
    const file = new File([PNG_1080_BUFFER], 'test.png', { type: 'image/png' });
    const rejection = await validateOne(file, 'image');
    expect(rejection).toBeNull();
  });
});

